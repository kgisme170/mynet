public class User {
    private int Id;
    private String Username;
    private String Password;

    public int getId() {
        return Id;
    }

    public void setId(int i) {
        Id = i;
    }

    public String getUsername() {
        return Username;
    }

    public void setUsername(String u) {
        Username = u;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String p) {
        Password = p;
    }
}
